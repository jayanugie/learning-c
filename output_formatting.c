#include <stdio.h>

int main() {
	
	// DATA TYPE
	
	// integer (int)
	int integerNumber = 103;
	printf("Sertaus Tiga = %d\n", integerNumber);
	
	// character (char)
	char character = 'A';
	printf("Huruf A = %c\n", character);
	
	// pecahan - floating-point (double)
	double doubleNumber = 3.14;
	printf("Nilai Pi = %lf\n", doubleNumber);
	
	// string (array of characters)
	char string[] = "Gabungan dari setiap karakter";
	printf("String = %s\n", string);
	
	// booelan
	int booleanTrue = 1;
	int booleanFalse = 0;
	printf("True = %d | False = %d \n", booleanTrue, booleanFalse);	
	
	return 0;
}
